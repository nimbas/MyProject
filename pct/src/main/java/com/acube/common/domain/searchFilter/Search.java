package com.acube.common.domain.searchFilter;

import java.io.Serializable;

/**
 * Mybatis의 mapper xml에서 검색 조건으로 사용될 필터항목
 *
 * @author YangByeongSub
 * @since
 *
 */
public class Search implements Serializable {

	private static final long serialVersionUID = -2258246213673005191L;
	private String name;
	private String type;
	private String value;

	public Search() {

	}

	public Search(String name, String type, String value) {
		this.name = name;
		this.type = type;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}