package com.acube.pvs.domain.monitor;

import java.io.Serializable;

import com.acube.common.domain.BaseDomain;

public class PvsProcessTree extends BaseDomain implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 2043623620919504396L;
	
	private String hostName;
	private String name;
	private String processName;
	private String status;
	private String operatorId;
	private String dateTime;
	private String startCmd;
	private String stopCmd;
	private String statusDt;
	private String errorMsg;  // just-error
	
	
	private String id;
	private String parent;
	private int lvl;
	private int level;
	private boolean isIsLeaf;
	private boolean expanded;
	private boolean loaded;
	
	
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProcessName() {
		return processName;
	}
	public void setProcessName(String processName) {
		this.processName = processName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getOperatorId() {
		return operatorId;
	}
	public void setOperatorId(String operatorId) {
		this.operatorId = operatorId;
	}
	public String getDateTime() {
		return dateTime;
	}
	public void setDateTime(String dateTime) {
		this.dateTime = dateTime;
	}
	public String getStartCmd() {
		return startCmd;
	}
	public void setStartCmd(String startCmd) {
		this.startCmd = startCmd;
	}
	public String getStopCmd() {
		return stopCmd;
	}
	public void setStopCmd(String stopCmd) {
		this.stopCmd = stopCmd;
	}
	public String getStatusDt() {
		return statusDt;
	}
	public void setStatusDt(String statusDt) {
		this.statusDt = statusDt;
	}
	public String getErrorMsg() {
		return errorMsg;
	}
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getParent() {
		return parent;
	}
	public void setParent(String parent) {
		this.parent = parent;
	}
	public int getLvl() {
		return lvl;
	}
	public void setLvl(int lvl) {
		this.lvl = lvl;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public boolean isIsLeaf() {
		return isIsLeaf;
	}
	public void setIsLeaf(boolean isIsLeaf) {
		this.isIsLeaf = isIsLeaf;
	}
	public boolean isLoaded() {
		return loaded;
	}
	public void setLoaded(boolean loaded) {
		this.loaded = loaded;
	}
	/**
	 * @return the expanded
	 */
	public boolean isExpanded() {
		return expanded;
	}
	/**
	 * @param expanded the expanded to set
	 */
	public void setExpanded(boolean expanded) {
		this.expanded = expanded;
	}
	@Override
	public String toString() {
		return "PvsProcessTree [hostName=" + hostName + ", name=" + name + ", processName=" + processName + ", status=" + status + ", operatorId=" + operatorId + ", dateTime=" + dateTime
				+ ", startCmd=" + startCmd + ", stopCmd=" + stopCmd + ", statusDt=" + statusDt + ", errorMsg=" + errorMsg + ", id=" + id + ", parent=" + parent + ", lvl=" + lvl + ", level=" + level
				+ ", isIsLeaf=" + isIsLeaf + ", expanded=" + expanded + ", loaded=" + loaded + "]";
	}
	
	
}
